public class Data_Types_Arithmetic {
    public static void main(String[] args) {
        byte b = 25;
        short s = 10;
        int i = 8563;
        long l = 12547862L;
        float f = 203.25F;
        double d = 125463.36589D;
        char c = 'S';

        System.out.println("Integer = " + b + s);
        System.out.println("Integer = " + s + i);
        System.out.println("Float = " + l + f);
        System.out.println("Float = " + i + f);
        System.out.println("Integer = " + c + i);
        System.out.println("Integer = " + c + s);
        System.out.println("Double = " + l + d);
        System.out.println("Double = " + f + d);
    }
}
