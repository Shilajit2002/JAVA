// Primitive Data Types

public class Primitive {
    public static void main(String[] args) {
        byte a = 10;
        short b = 20;
        int c = 58693;
        long d = 5551255782862548L;
        float e = 10236.253f;
        double f = 789563.25413;
        char g = 'S';
        String h = "Shilajit";
        boolean i = true;
        boolean j = false;

        System.out.println(
                a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h + " " + i + " " + j + " ");
    }
}
