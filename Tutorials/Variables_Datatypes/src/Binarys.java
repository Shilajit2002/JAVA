public class Binarys {
    public static void main(String[] args) {
        //  Binary
        int bin=0b10101111110;

        //  Using Separator
        int bin1=0b1010_1111_1110;

        System.out.println(bin);
        System.out.println(bin1);

        //  Octal
        int oct=01234;

        System.out.println(oct);

        //  Hexadecimal
        int hex=0xBAD;
        System.out.println(hex);
    }
}
